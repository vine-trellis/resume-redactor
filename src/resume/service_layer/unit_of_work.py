# pylint: disable=attribute-defined-outside-init
from __future__ import annotations
import abc
from sqlalchemy import create_engine
from sqlalchemy.orm import sessionmaker
from sqlalchemy.orm.session import Session


from common import config
from common.adapters import readonly_repository as ro_repo

from resume.adapters import repository
from resume.domain import model


class AbstractUnitOfWork(abc.ABC):
    resumes: repository.AbstractRepository
    prospects: ro_repo.AbstractReadOnlyRepository

    def __enter__(self) -> AbstractUnitOfWork:
        return self

    def __exit__(self, *args):
        self.rollback()

    def commit(self):
        self._commit()

    def collect_new_events(self):
        for resume in self.resumes.seen:
            while resume.events:
                yield resume.events.pop(0)

    @abc.abstractmethod
    def _commit(self):
        raise NotImplementedError

    @abc.abstractmethod
    def rollback(self):
        raise NotImplementedError


DEFAULT_SESSION_FACTORY = sessionmaker(
    bind=create_engine(
        config.get_postgres_uri(), isolation_level="REPEATABLE READ", max_overflow=20
    )
)


class SqlAlchemyUnitOfWork(AbstractUnitOfWork):
    def __init__(self, session_factory=DEFAULT_SESSION_FACTORY):
        self.session_factory = session_factory

    def __enter__(self):
        self.session = self.session_factory()  # type: Session
        self.resumes = repository.SqlAlchemyRepository(self.session)
        self.prospects = ro_repo.SqlAlchemyReadOnlyRepository(
            self.session, model.Prospect
        )
        return super().__enter__()

    def __exit__(self, *args):
        super().__exit__(*args)
        self.session.close()

    def _commit(self):
        self.session.commit()

    def rollback(self):
        self.session.rollback()
